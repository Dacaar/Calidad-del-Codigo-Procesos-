public class Ifanidados{

    public static void main (String [] args){
        int i = 0;
        int j = 1;
        int k = 2;
        int f = 20;
        
        while (i=0){
            if (j<f){
                if (k>j){
                    if (k < f){
                        k++;
                    }
                }
            }
            j++;
         }
     }
 }
             
